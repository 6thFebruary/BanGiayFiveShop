    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
       DefaultTableModel dfm = new DefaultTableModel();
       int index = tblKhachHang.getSelectedRow();
       int i = 0 ;
       txtMa.setText(dfm.getValueAt(index, 0).toString());
       txtTen.setText(dfm.getValueAt(index, 1).toString());
       txtNgaySinh.setText(dfm.getValueAt(index, 2).toString());
       txtSdt.setText(dfm.getValueAt(index, 4).toString());
       txtEmail.setText(dfm.getValueAt(index, 5).toString());
       txtDiaChi.setText(dfm.getValueAt(index, 6).toString());
        if (dfm.getValueAt(index, 3).toString().equals("Nam")) {
            rdoNam.setSelected(true);
        }else{
            rdoNu.setSelected(true);
        }
        if (dfm.getValueAt(index, 7).toString().equals("Hoạt Động")) {
            rdoHoatDong.setSelected(true);
        }else{
            rdoKhongHoatDong.setSelected(true);
        }
    }//GEN-LAST:event_tblKhachHangMouseClicked

