    public void loadTable(){
        ArrayList<KhachHang> kh = this.khRepo.getList();
        DefaultTableModel dfm = (DefaultTableModel) this.tblKhachHang.getModel();
        dfm.setRowCount(0);
        for (KhachHang khm : kh) {
            Object[] row = {
                khm.getMa(),
                khm.getTen(),
                khm.getNgaySinh(),
                khm.getGioiTinh() == 1? "Nam":"Nu",
                khm.getSdt(),
                khm.getEmail(),
                khm.getDiaChi(),
                khm.getTrangThai()== 1? "Hoat Dong" : "Khong Hoat Dong",
            };
            dfm.addRow(row);
        }
    }
    
    

