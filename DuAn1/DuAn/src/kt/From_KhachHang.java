    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String gioiTinh = rdoNam.isSelected() ? "1" : "0";
        String trangThai = rdoHoatDong.isSelected() ? "1" : "0";
        KhachHang kh = new KhachHang();
        kh.setMa(txtMa.getText());
        kh.setTen(txtTen.getText());
        kh.setEmail(txtEmail.getText());
        try {
    SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
    kh.setNgaySinh(sdf.parse(txtNgaySinh.getText()));
        } catch (Exception e) {
            e.printStackTrace(); 
        }
        kh.setGioiTinh(Integer.parseInt(gioiTinh));
        kh.setTrangThai(Integer.parseInt(trangThai));
        kh.setDiaChi(txtDiaChi.getText());
        kh.setSdt(txtSdt.getText());
        try {
            khRepo.addNew(kh);
            JOptionPane.showMessageDialog(this, "Thêm thành công!!!");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Thêm thát bại :<");
        }
        loadTable();
    }//GEN-LAST:event_btnThemActionPerformed

